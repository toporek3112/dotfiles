    hl.exec_cmd(apps.terminal)
    hl.exec_cmd("waybar")
    hl.exec_cmd("hyprshot")
    hl.exec_cmd("swaync")
    hl.exec_cmd("hyprpaper")
    hl.exec_cmd("firefox")
    hl.exec_cmd("flatpak run com.spotify.Client")
    hl.exec_cmd("obsidian")
end)


